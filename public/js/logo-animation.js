function initAnimation(logo) {
    let isAnimated = false;
    if(!logo.length){
        return;
    }
    const darkBlue = logo.find('[data-logo-dark-blue]');
    const lightBlue = logo.find('[data-logo-light-blue]');
    if(isAnimated){
        return;
    }

    isAnimated = true;

    // dark blue animation
    anime({
        targets: darkBlue[0],
        d: [
            {
                value: [
                    darkBlueValues[1],
                    darkBlueValues[2]
                ],
                duration: 500,
            },
            {
                value: [
                    darkBlueValues[0],
                    darkBlueValues[1]
                ],
                duration: 500
            }
        ],
        opacity:[
            {
                value: [1, 0],
                duration: 500
            },
            {
                value: [0, 1],
                duration: 250
            }
        ],
        easing: 'cubicBezier(.64,0,.36,1)'
    });

    // light blue
    anime({
        targets: lightBlue[0],
        d: [
            {
                value: [
                    lightBlueValues[1],
                    lightBlueValues[2]
                ],
                duration: 500,
                delay: 160,
            },
            {
                value: [
                    lightBlueValues[0],
                    lightBlueValues[1]
                ],
                duration: 500,
            }
        ],
        opacity:[
            {
                value: [1, 0],
                duration: 250,
                delay: 410,
            },
            {
                value: [0, 1],
                duration: 250,
            }
        ],
        easing: 'cubicBezier(.64,0,.36,1)',
        complete(){
            isAnimated = false;
        }
    });
}

$(document).ready(function()
{
    $('[data-logo]').each(function(){
        $(this).mouseenter((event) => {
            initAnimation($(this));
        })
    });
})

